#include <iostream>
// C++ has 3 floating-point types: float, double, long double

int main() {
  double pi = 3.14; // Declares and initialises a variable "pi" with the value "3.14"
  std::cout << "The value of pi is: " << pi;
  // Floats can also include
  double x = 213.456;
  double y = 1.;
  double z = 0.15;
  double w = .324;
  double v = 3.14e10;
}